import 'package:cached_network_image/cached_network_image.dart';
import 'package:ditonton/common/constants.dart';
import 'package:ditonton/domain/entities/movie.dart';
import 'package:ditonton/presentation/pages/movie_detail_page.dart';
import 'package:ditonton/tv_show/domain/entities/tv_show.dart';
import 'package:flutter/material.dart';

class MovieCard extends StatelessWidget {
  final Movie? movie;
  final TvShow? tvShow;
  final bool isTvShow;

  MovieCard(this.movie, this.isTvShow, this.tvShow);

  @override
  Widget build(BuildContext context) {
    final id = isTvShow ? tvShow?.id : movie?.id;
    final title = isTvShow ? tvShow?.name : movie?.title;
    final overview = isTvShow ? tvShow?.overview : movie?.overview;
    final posterPath = isTvShow ? tvShow?.posterPath : movie?.posterPath;
    return Container(
      margin: const EdgeInsets.symmetric(vertical: 4),
      child: InkWell(
        onTap: () {
          Navigator.pushNamed(
            context,
            MovieDetailPage.ROUTE_NAME,
            arguments: {"id": id, "isTvShow": isTvShow},
          );
        },
        child: Stack(
          alignment: Alignment.bottomLeft,
          children: [
            Card(
              child: Container(
                margin: const EdgeInsets.only(
                  left: 16 + 80 + 16,
                  bottom: 8,
                  right: 8,
                ),
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Text(
                      title ?? '-',
                      maxLines: 1,
                      overflow: TextOverflow.ellipsis,
                      style: kHeading6,
                    ),
                    SizedBox(height: 16),
                    Text(
                      overview ?? '-',
                      maxLines: 2,
                      overflow: TextOverflow.ellipsis,
                    ),
                  ],
                ),
              ),
            ),
            Container(
              margin: const EdgeInsets.only(
                left: 16,
                bottom: 16,
              ),
              child: ClipRRect(
                child: CachedNetworkImage(
                  key: ValueKey('movie_card_$id'),
                  imageUrl: '$BASE_IMAGE_URL${posterPath}',
                  width: 80,
                  placeholder: (context, url) => Center(
                    child: CircularProgressIndicator(),
                  ),
                  errorWidget: (context, url, error) => Icon(Icons.error),
                ),
                borderRadius: BorderRadius.all(Radius.circular(8)),
              ),
            ),
          ],
        ),
      ),
    );
  }
}
